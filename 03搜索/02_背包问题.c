#include<stdio.h>
#include<math.h>
int W[22],sum=0,N=0,minmize=10000,temp;

int dfs(int cur,int total)
{
	temp=fabs((sum-total)-total);
	if(temp<minmize)
		minmize=temp;
	if(cur==N || total>sum/2)
		return 0;
	dfs(cur+1,total+W[cur]); //第一种情况，当前物品放入背包，所以计入总价值 
	dfs(cur+1,total); // 第二种情况，当前物品不放入背包 
}

int main()
{
	int i;
 	while(scanf("%d",&N)!=EOF)
 	{
		for(i=0;i<N;i++)
		{ 
			scanf("%d",&W[i]);
			sum+=W[i]; 
		} 
		dfs(0,0);
		printf("%d\n",minmize);
 	}
 	return 0;
} 
  
