#include<stdio.h>
#include<string.h>
int max(int a,int b)
{
	return a>b?a:b ;
}
int main()
{
	int W[25],sum=0,N=0,weight[100005];
	int i,j,temp;
	memset(weight,0,sizeof(weight));
 	while(scanf("%d",&N)!=EOF)
 	{
		for(i=0;i<N;i++)
		{ 
			scanf("%d",&W[i]);
			sum+=W[i]; 
		}
		temp=sum/2;
		for(i=0;i<N;i++)    	//动态规划算法 
		{
			for(j=temp;j>=W[i];j--)
			{
				weight[j]=max(weight[j-1],weight[j-W[i]]+W[i]);
			}
		}
		temp=(sum-weight[temp])-weight[temp];
		printf("%d\n",temp);
 	}
 	return 0;
} 
  
