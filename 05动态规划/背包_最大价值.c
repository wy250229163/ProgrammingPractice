#include<stdio.h>
#include<string.h>

#define max(a,b) a>b?a:b

int sum[30010];

int main(void)
{
	int N;
	scanf("%d",&N);
	while(N--)
	{
		int n,m;
		int i,j;
		scanf("%d%d",&n,&m);
		int price,value;
		memset(sum,0,sizeof(sum));
		for(i=0;i<m;i++)
		{
			scanf("%d%d",&price,&value);
			for(j=n;j>=price;j--)
			{
				sum[j]=max(sum[j],sum[j-price]+price*value);
			}
		}
		printf("%d\n",sum[n]);
	} 
	return 0;
}

